package codeforces.round956_;
import java.util.*;

public class ArrayDivisibility {

    public static void main(String[] args){
        Scanner scx = new Scanner(System.in);
        int T = scx.nextInt();
        while(T-->0){
            int n = scx.nextInt();
            for(int i = 1 ; i<= n ; i++){
                System.out.print(i+" ") ; 
            }
            System.out.println();
        }
    }

}
